const SYMBOL_NAMES = new Map<string, string>();

for (const name of NAMES) {
  let symbol: string = '';

  for (let index = 0; index < name.length; index++) {
    const char = name.charAt(index).toUpperCase();

    if (char === ' ') {
      continue;
    }

    symbol = `${symbol}${char}`;

    if (symbol.length >= 3 && !SYMBOL_NAMES.has(symbol)) {
      break;
    }
  }

  SYMBOL_NAMES.set(symbol, name);
}

export const TOKENS_METADATA = Array.from(SYMBOL_NAMES.entries()).map(
  ([symbol, name]) => ({
    symbol,
    name,
  }),
);
